public interface Delimiter {
    /** 英文逗号分隔 */
    String COMMA = ",";

    /** 英文链接符号 */
    String JOINER = "|";

    /** 英文分号分隔 */
    String SEMICOLON = ";";

    /** 英文下划线 */
    String UNDERLINE = "_";

    /** 英文横岗 */
    String HORIZONTAL_LINE = "-";

    /** 英文点号 */
    String POINT = ".";

    /** 英文空格 */
    String BLANK = " ";

    /** 百分号 */
    String PERCENT = "%";

    /** 星号 */
    String ASTERISK = "*";
}
